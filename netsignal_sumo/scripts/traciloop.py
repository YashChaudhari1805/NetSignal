def _save_summary_plot(waiting_history, incident_injected, incident_time):
    """Saves a waiting time plot to output/summary_plot.png"""
    try:
        import matplotlib
        matplotlib.use("Agg")  # Non-interactive backend
        import matplotlib.pyplot as plt
        import matplotlib.patches as mpatches

        fig, ax = plt.subplots(figsize=(14, 5))
        timesteps = list(range(len(waiting_history)))

        ax.fill_between(timesteps, waiting_history, alpha=0.2, color="#2E75B6")
        ax.plot(timesteps, waiting_history, color="#2E75B6", linewidth=1.5,
                label="Network waiting vehicles")

        # Mark EV event
        if 900 < len(waiting_history):
            ax.axvline(x=900, color="red", linestyle="--", linewidth=1.5, alpha=0.8)
            ax.text(910, max(waiting_history) * 0.9, "EV dispatched",
                    color="red", fontsize=9)

        # Mark incident
        if incident_injected and incident_time < len(waiting_history):
            ax.axvline(x=incident_time, color="orange", linestyle="--",
                       linewidth=1.5, alpha=0.8)
            ax.text(incident_time + 10, max(waiting_history) * 0.7, "Incident injected",
                    color="orange", fontsize=9)

        # Rolling average
        window = 30
        if len(waiting_history) > window:
            rolling = np.convolve(waiting_history, np.ones(window)/window, mode='valid')
            ax.plot(range(window-1, len(waiting_history)), rolling,
                    color="#C00000", linewidth=2, linestyle="-",
                    label=f"{window}s rolling average")

        ax.set_xlabel("Simulation Time (seconds)", fontsize=11)
        ax.set_ylabel("Total Waiting Vehicles", fontsize=11)
        ax.set_title("NetSignal — Network Waiting Vehicles Over Time\n"
                     "(Fixed-time baseline — replace with GNN for improvement)",
                     fontsize=12, fontweight="bold")
        ax.legend(fontsize=10)
        ax.grid(True, alpha=0.3)
        ax.set_xlim(0, len(waiting_history))
        ax.set_ylim(0, max(waiting_history) * 1.15 if waiting_history else 10)

        # Annotate stats
        stats_text = (
            f"Avg: {np.mean(waiting_history):.1f}  |  "
            f"Peak: {max(waiting_history)}  |  "
            f"Final: {waiting_history[-1]}"
        )
        ax.text(0.99, 0.02, stats_text, transform=ax.transAxes,
                fontsize=9, ha="right", color="#404040",
                bbox=dict(boxstyle="round,pad=0.3", facecolor="white", alpha=0.8))

        plt.tight_layout()
        out_path = OUTPUT_DIR / "summary_plot.png"
        plt.savefig(out_path, dpi=150, bbox_inches="tight")
        plt.close()
        print(f"  Plot saved: {out_path}")

    except ImportError:
        print("  (matplotlib not installed — skipping plot)")
    except Exception as e:
        print(f"  (Plot generation failed: {e})")


