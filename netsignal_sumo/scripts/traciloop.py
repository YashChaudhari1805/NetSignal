def print_dashboard(step, total_waiting, ev_active, incident_active, ev_path, state):
    """Prints a live status dashboard to the console every control cycle."""

    # Pick a few intersections to display
    sample_nodes = [_node_map.get(f"NODE_{c}_{r}", f"{c}/{r}") for c,r in [(0,0),(2,2),(3,3),(5,5),(3,1)]]

    print(f"\n{'═'*65}")
    print(f"  NetSignal Dashboard  |  Step: {step:>5}s  |  "
          f"{'🟢' if not ev_active else '🔴'} EV: {'ACTIVE' if ev_active else 'clear  '}"
          f"  |  {'⚠️  INCIDENT' if incident_active else '            '}")
    print(f"{'─'*65}")
    print(f"  Network total waiting: {total_waiting:>4} vehicles")

    if ev_active and ev_path:
        print(f"  EV corridor: {' → '.join(ev_path)}")

    print(f"{'─'*65}")
    print(f"  {'Intersection':<12} {'Waiting':>7} {'Vehicles':>8} {'Avg Speed':>10} {'Phase':>6}")
    print(f"  {'─'*12} {'─'*7} {'─'*8} {'─'*10} {'─'*6}")

    for node_id in sample_nodes:
        if node_id in state:
            s = state[node_id]
            phase_labels = {0: "EW 🟢", 1: "EW 🟡", 2: "NS 🟢", 3: "NS 🟡"}
            phase_str = phase_labels.get(s["current_phase"], f"P{s['current_phase']}")
            speed_str = f"{s['avg_speed']:.1f} m/s"
            print(f"  {node_id:<12} {s['waiting']:>7} {s['vehicle_count']:>8} {speed_str:>10} {phase_str:>6}")

    print(f"{'═'*65}")


# ══════════════════════════════════════════════════════════
#   MAIN TRACI LOOP
# ══════════════════════════════════════════════════════════

def run_simulation(gui=True, inject_incident=False, incident_time=600,
                   incident_lane="e_2_2_2_3_0"):
    """
    Main simulation loop.

    Parameters
    ----------
    gui            : bool  — launch sumo-gui (True) or headless sumo (False)
    inject_incident: bool  — inject a simulated incident for training data
    incident_time  : int   — timestep to inject the incident
    incident_lane  : str   — lane ID to block
    """

    # ── 1. Launch SUMO ──
    sumo_binary = "sumo-gui" if gui else "sumo"
    VIEW_FILE = PROJECT_DIR / "config" / "netsignal.view.xml"

    sumo_cmd = [
        sumo_binary,
        "-c", str(CONFIG_FILE),
        # NOTE: --remote-port and --start are handled automatically by traci.start()
        # NOTE: --quit-on-end removed — it conflicts with quit-on-end=false in sumocfg
    ]

    # Pass view settings file for GUI mode (handles viewport, breakpoints etc.)
    if gui and VIEW_FILE.exists():
        sumo_cmd += ["-g", str(VIEW_FILE)]

    # Always suppress route errors at command level too (belt-and-suspenders)
    sumo_cmd += ["--ignore-route-errors"]
    if not gui:
        sumo_cmd += ["--no-step-log", "--no-warnings"]

    print(f"\n{'═'*65}")
    print(f"  NetSignal Traffic Simulation Starting")
    print(f"{'─'*65}")
    print(f"  Mode       : {'GUI (sumo-gui)' if gui else 'Headless (sumo)'}")
    print(f"  Config     : {CONFIG_FILE}")
    print(f"  Duration   : {SIM_DURATION}s (1 hour)")
    print(f"  Control    : Fixed-time baseline (replace with GNN later)")
    print(f"  Hospital   : {HOSPITAL_NODE}")
    print(f"  EV depart  : t=900s")
    if inject_incident:
        print(f"  Incident   : t={incident_time}s on lane {incident_lane}")
    print(f"{'═'*65}\n")

    # ── 2. Ensure output directory exists before SUMO tries to write to it ──
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

    # ── 3. Launch SUMO and connect TraCI in one synchronized call ──
    # traci.start() is the recommended approach — it handles process launch,
    # port binding, and connection handshake atomically (no race conditions).
    print(f"  Launching: {' '.join(sumo_cmd)}\n")
    sumo_process = None
    sumo_stderr_lines = []  # traci.start() captures output internally
    try:
        traci.start(sumo_cmd, port=TRACI_PORT)
        print("  ✓ TraCI connected successfully\n")
    except traci.exceptions.FatalTraCIError as e:
        print(f"  ✗ TraCI/SUMO launch failed: {e}")
        print("    Check that SUMO is installed and on your PATH.")
        print("    Download: https://sumo.dlr.de/docs/Downloads.php")
        return
    except FileNotFoundError:
        print(f"  ✗ Could not find '{sumo_binary}'. Is SUMO installed and on your PATH?")
        print("    Download: https://sumo.dlr.de/docs/Downloads.php")
        return
    except Exception as e:
        print(f"  ✗ Failed to start SUMO: {e}")
        return

    # ── 4. Initialise components ──
    road_graph  = build_road_graph()
    controller  = FixedTimeController(DEFAULT_GREEN_DURATION, DEFAULT_YELLOW_DURATION)
    preemptor   = EVPreemptor(road_graph)
    logger      = DataLogger()
    incident    = IncidentInjector(incident_lane, incident_time) if inject_incident else None

    # Subscribe all intersections for efficient state reading
    for node_id in ALL_INTERSECTIONS:
        try:
            traci.junction.subscribeContext(
                node_id, tc.CMD_GET_VEHICLE_VARIABLE, 100,
                [tc.VAR_SPEED, tc.VAR_LANE_ID]
            )
        except Exception:
            pass

    # Metrics tracking
    waiting_history = []
    start_wall_time = time.time()

    # ── 5. Main simulation loop ──
    try:
        for step in range(SIM_DURATION):

            # Advance simulation by one step
            try:
                traci.simulationStep()
            except traci.exceptions.FatalTraCIError as e:
                print(f"\n  SUMO disconnected at step {step}: {e}")
                print("  Check the SUMO terminal/window for error details.")
                break

            # Read full network state
            state, emergency_vehicles, total_waiting = read_network_state()
            waiting_history.append(total_waiting)

            ev_active       = len(emergency_vehicles) > 0
            incident_active = incident.step(step) if incident else False

            # Log state every step
            logger.log_state(step, state, ev_active, incident_active)

            # Log incident ground truth
            if incident_active:
                logger.log_incident(step, incident_lane, "blockage")

            # ── Control logic every CONTROL_FREQ steps ──
            if step % CONTROL_FREQ == 0:

                # Update EV preemptor
                preemptor.update(emergency_vehicles)

                # Get fixed-time actions (placeholder for GNN)
                actions = controller.get_actions(state)

                # Override preempted intersections
                preempted = preemptor.get_preempted_intersections()
                for node_id in preempted:
                    ev_phase = preemptor.get_phase_for_intersection(node_id)
                    if ev_phase is not None:
                        actions[node_id] = ev_phase

                # Apply all phase actions to SUMO
                for node_id, phase in actions.items():
                    try:
                        current = traci.trafficlight.getPhase(node_id)
                        # Only change if different and not mid-yellow
                        if current != phase and current not in [PHASE_EW_YELLOW, PHASE_NS_YELLOW]:
                            traci.trafficlight.setPhase(node_id, phase)
                    except traci.exceptions.TraCIException:
                        pass

                # Log network metrics
                logger.log_metrics(step, total_waiting, state, ev_active, incident_active)

                # Print dashboard every 30 steps (every 30 seconds sim time)
                if step % 30 == 0:
                    print_dashboard(
                        step, total_waiting, ev_active, incident_active,
                        preemptor.path, state
                    )

    except KeyboardInterrupt:
        print("\n\n  Simulation interrupted by user.")

    finally:
        # ── 6. Cleanup ──
        wall_time = time.time() - start_wall_time
        print(f"\n{'═'*65}")
        print(f"  Simulation Complete")
        print(f"{'─'*65}")
        print(f"  Sim time       : {SIM_DURATION}s")
        rt = f"{SIM_DURATION/wall_time:.1f}x" if wall_time > 0.01 else "N/A"
        print(f"  Wall time      : {wall_time:.1f}s ({rt} real-time)")
        if waiting_history:
            print(f"  Avg waiting    : {np.mean(waiting_history):.1f} vehicles")
            print(f"  Peak waiting   : {max(waiting_history)} vehicles")
        else:
            print("  Avg waiting    : N/A (simulation did not run)")
            print("  Peak waiting   : N/A")
        print(f"  Data logged to : {OUTPUT_DIR}")
        print(f"{'═'*65}\n")

        try:
            traci.close()
        except Exception:
            pass
        # Note: when using traci.start(), SUMO process is managed by traci internally

        # Save summary plot
        if waiting_history:
            _save_summary_plot(waiting_history, inject_incident, incident_time)


