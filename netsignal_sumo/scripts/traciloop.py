ALL_INTERSECTIONS = [n for n in ALL_INTERSECTIONS if n]  # filter blanks

# Fixed-time cycle for baseline (used when no ML model is active)
DEFAULT_GREEN_DURATION = 30   # seconds
DEFAULT_YELLOW_DURATION = 3   # seconds


# ══════════════════════════════════════════════════════════
#   ROAD GRAPH (for Dijkstra EV routing)
# ══════════════════════════════════════════════════════════

def build_road_graph():
    """
    Build a NetworkX directed graph of the 6x6 grid.
    Nodes = intersections (n_r_c)
    Edges = road segments with weight = length (300m each)

    This graph is used for Dijkstra path finding when an
    emergency vehicle is detected.
    """
    G = nx.DiGraph()

    for r in range(GRID_SIZE):
        for c in range(GRID_SIZE):
            G.add_node(f"{c}/{r}", pos=(c * 300, r * 300))

    # Add edges for every adjacent pair (horizontal and vertical)
    for r in range(GRID_SIZE):
        for c in range(GRID_SIZE - 1):
            # Horizontal bidirectional
            G.add_edge(f"{c}/{r}",   f"{c+1}/{r}", weight=300, edge_id=f"{c}/{r}-{c+1}/{r}")
            G.add_edge(f"{c+1}/{r}", f"{c}/{r}",   weight=300, edge_id=f"{c+1}/{r}-{c}/{r}")

    for r in range(GRID_SIZE - 1):
        for c in range(GRID_SIZE):
            # Vertical bidirectional
            G.add_edge(f"{c}/{r}",   f"{c}/{r+1}", weight=300, edge_id=f"{c}/{r}-{c}/{r+1}")
            G.add_edge(f"{c}/{r+1}", f"{c}/{r}",   weight=300, edge_id=f"{c}/{r+1}-{c}/{r}")

    return G


# ══════════════════════════════════════════════════════════
#   STATE READER
# ══════════════════════════════════════════════════════════

def read_network_state():
    """
    Read the full network state from SUMO via TraCI.

    Returns a dict keyed by intersection ID containing:
      - lanes         : list of lane IDs approaching this intersection
      - waiting       : total waiting vehicles (speed < 0.1 m/s)
      - queue         : total queued vehicles
      - avg_speed     : mean speed across all approaching lanes (m/s)
      - vehicle_count : total vehicles on approaching lanes
      - current_phase : active signal phase index

    Also returns:
      - emergency_vehicles : dict { veh_id -> lane_id } for any EV_ vehicles
      - total_waiting      : sum of waiting vehicles across all intersections
    """
    state = {}
    emergency_vehicles = {}
    total_waiting = 0

    # Get all vehicle speeds and positions in one call (efficient)
    try:
        all_lane_vehicles = traci.lane.getAllSubscriptionResults()
    except Exception:
        all_lane_vehicles = {}

    for node_id in ALL_INTERSECTIONS:
        try:
            # Get all incoming lanes for this intersection
            incoming_lanes = traci.junction.getContextSubscriptionResults(node_id)

            # Fallback: get lanes from the trafficlight
            lane_ids = traci.trafficlight.getControlledLanes(node_id)
            lane_ids = list(set(lane_ids))  # deduplicate

            waiting_count  = 0
            vehicle_count  = 0
            speed_sum      = 0.0
            speed_readings = 0

            for lane_id in lane_ids:
                try:
                    waiting  = traci.lane.getLastStepHaltingNumber(lane_id)
                    vehicles = traci.lane.getLastStepVehicleNumber(lane_id)
                    speed    = traci.lane.getLastStepMeanSpeed(lane_id)

                    waiting_count  += waiting
                    vehicle_count  += vehicles
                    speed_sum      += speed
                    speed_readings += 1

                    # Check each vehicle on this lane for EV prefix
                    veh_ids = traci.lane.getLastStepVehicleIDs(lane_id)
                    for vid in veh_ids:
                        if vid.startswith(EV_PREFIX):
                            emergency_vehicles[vid] = lane_id

                except traci.exceptions.TraCIException:
                    pass  # Lane may not exist yet — ignore

            avg_speed = speed_sum / speed_readings if speed_readings > 0 else 0.0

            try:
                current_phase = traci.trafficlight.getPhase(node_id)
            except Exception:
                current_phase = -1

            state[node_id] = {
                "lanes":         lane_ids,
                "waiting":       waiting_count,
                "queue":         waiting_count,  # proxy — refine later with actual queue length
                "avg_speed":     round(avg_speed, 3),
                "vehicle_count": vehicle_count,
                "current_phase": current_phase,
            }

            total_waiting += waiting_count

        except traci.exceptions.TraCIException:
            state[node_id] = {
                "lanes": [], "waiting": 0, "queue": 0,
                "avg_speed": 0.0, "vehicle_count": 0, "current_phase": -1
            }

    return state, emergency_vehicles, total_waiting


# ══════════════════════════════════════════════════════════
#   FIXED-TIME CONTROLLER (baseline)
# ══════════════════════════════════════════════════════════

class FixedTimeController:
    """
    Simple fixed-time signal controller.
    All intersections cycle through EW-green → EW-yellow → NS-green → NS-yellow
    on a fixed schedule.

    This is your baseline. Your GNN will replace this class.
    """
    def __init__(self, green_duration=30, yellow_duration=3):
        self.green_dur  = green_duration
        self.yellow_dur = yellow_duration
        self.cycle_len  = 2 * (green_duration + yellow_duration)
        self.step       = 0

    def get_actions(self, state):
        """Returns {intersection_id: phase_index} for all intersections."""
        actions = {}
        offset = 0
        for node_id in ALL_INTERSECTIONS:
            # Stagger intersections by a small offset to avoid all switching at once
            cycle_pos = (self.step + offset) % self.cycle_len
            if   cycle_pos < self.green_dur:
                phase = PHASE_EW_GREEN
            elif cycle_pos < self.green_dur + self.yellow_dur:
                phase = PHASE_EW_YELLOW
            elif cycle_pos < 2 * self.green_dur + self.yellow_dur:
                phase = PHASE_NS_GREEN
            else:
                phase = PHASE_NS_YELLOW
            actions[node_id] = phase
            offset = (offset + 2) % 8  # small stagger per intersection
        self.step += 1
        return actions


# ══════════════════════════════════════════════════════════
#   EMERGENCY VEHICLE PREEMPTION
# ══════════════════════════════════════════════════════════

class EVPreemptor:
    """
    When an emergency vehicle is detected, computes the shortest path
    from its current position to the hospital node and preempts all
    intersections along that path with a green phase toward the EV.

    Uses NetworkX Dijkstra on the road graph.
    """
    def __init__(self, road_graph):
        self.graph   = road_graph
        self.active  = False
        self.path    = []
        self.ev_id   = None

    def update(self, emergency_vehicles):
        """
        Called each control cycle with the dict of detected EVs.
        Updates the preemption path if an EV is present.
        """
        if not emergency_vehicles:
            if self.active:
                print("\n  ✓ EV has cleared the network — releasing preemption")
            self.active = False
            self.path   = []
            self.ev_id  = None
            return

        # Take the first EV found (extend later for multi-EV)
        ev_id, current_lane = next(iter(emergency_vehicles.items()))

        if ev_id != self.ev_id:
            self.ev_id = ev_id
            print(f"\n  🚨 EMERGENCY VEHICLE DETECTED: {ev_id}")
            print(f"     Current lane: {current_lane}")

        # Convert lane ID to node ID
        # Lane format: e_{r1}_{c1}_{r2}_{c2}_{lane_index}
        # → source node = n_{r1}_{c1}
        current_node = self._lane_to_node(current_lane, ev_id)
        if current_node is None:
            return

        try:
            path = nx.dijkstra_path(self.graph, source=current_node, target=HOSPITAL_NODE)
            self.path   = path
            self.active = True
            print(f"     Preemption corridor: {' → '.join(path)}")
        except nx.NetworkXNoPath:
            print(f"     WARNING: No path from {current_node} to {HOSPITAL_NODE}")
            self.active = False

    def _lane_to_node(self, lane_id, ev_id):
        """
        Extract source node from a lane/edge ID.
        netgenerate edge format: "A0-B0" (fromNode-toNode)
        Lane format: "A0-B0_0" (edge_id + _lane_index)
        Source node: part before first '-' e.g. "A0"
        """
        try:
            road_id = traci.vehicle.getRoadID(ev_id)
            if road_id and not road_id.startswith(":") and "-" in road_id:
                return road_id.split("-")[0]
        except Exception:
            pass

        # Fallback: parse from lane_id
        if lane_id and "-" in lane_id:
            edge_id = lane_id.rsplit("_", 1)[0]  # strip lane index
            return edge_id.split("-")[0]

        return None

    def get_preempted_intersections(self):
        """Returns the set of intersection IDs that should be preempted."""
        return set(self.path) if self.active else set()

    def get_phase_for_intersection(self, node_id):
        """
        Returns the phase index that gives green in the direction of EV travel.
        Determines direction by checking this node's position in the path.
        """
        if node_id not in self.path:
            return None

        idx = self.path.index(node_id)
        if idx >= len(self.path) - 1:
            return PHASE_NS_GREEN  # Last node — default

        current = node_id
        next_nd = self.path[idx + 1]

        # Parse col/row from node IDs using reverse lookup of _node_map
        rev = {v: (int(k.split("_")[1]), int(k.split("_")[2]))
               for k, v in _node_map.items() if k.startswith("NODE_")}
        c1, r1 = rev.get(current, (0, 0))
        c2, r2 = rev.get(next_nd,  (0, 0))

        # EV moving horizontally → give EW green
        if r1 == r2:
            return PHASE_EW_GREEN
        # EV moving vertically → give NS green
        else:
            return PHASE_NS_GREEN


