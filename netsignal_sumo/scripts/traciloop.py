class DataLogger:
    """
    Logs simulation state every step to CSV files.
    This data is used to train the BiLSTM incident detector.

    Output files:
      output/state_log.csv    — per-intersection state per timestep
      output/metrics_log.csv  — network-wide metrics per timestep
      output/incident_log.csv — ground truth incident labels
    """
    def __init__(self):
        ts = datetime.now().strftime("%Y%m%d_%H%M%S")
        self.state_file    = OUTPUT_DIR / f"state_log_{ts}.csv"
        self.metrics_file  = OUTPUT_DIR / f"metrics_log_{ts}.csv"
        self.incident_file = OUTPUT_DIR / f"incident_log_{ts}.csv"

        # Write CSV headers
        with open(self.state_file, "w", newline="") as f:
            writer = csv.writer(f)
            writer.writerow([
                "timestep", "intersection", "waiting", "queue",
                "avg_speed", "vehicle_count", "current_phase",
                "ev_active", "incident_active"
            ])

        with open(self.metrics_file, "w", newline="") as f:
            writer = csv.writer(f)
            writer.writerow([
                "timestep", "total_waiting", "total_vehicles",
                "avg_speed_network", "ev_active", "incident_active"
            ])

        with open(self.incident_file, "w", newline="") as f:
            writer = csv.writer(f)
            writer.writerow(["timestep", "lane", "label"])

        self.rows_buffered = []
        self.buffer_limit  = 100  # flush every 100 rows

    def log_state(self, timestep, state, ev_active, incident_active):
        with open(self.state_file, "a", newline="") as f:
            writer = csv.writer(f)
            for node_id, s in state.items():
                writer.writerow([
                    timestep, node_id,
                    s["waiting"], s["queue"], s["avg_speed"],
                    s["vehicle_count"], s["current_phase"],
                    int(ev_active), int(incident_active)
                ])

    def log_metrics(self, timestep, total_waiting, state, ev_active, incident_active):
        total_vehicles = sum(s["vehicle_count"] for s in state.values())
        speeds = [s["avg_speed"] for s in state.values() if s["avg_speed"] > 0]
        avg_speed_network = round(np.mean(speeds), 3) if speeds else 0.0

        with open(self.metrics_file, "a", newline="") as f:
            writer = csv.writer(f)
            writer.writerow([
                timestep, total_waiting, total_vehicles,
                avg_speed_network, int(ev_active), int(incident_active)
            ])

    def log_incident(self, timestep, lane, label):
        with open(self.incident_file, "a", newline="") as f:
            writer = csv.writer(f)
            writer.writerow([timestep, lane, label])


