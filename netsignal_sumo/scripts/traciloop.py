"""
NetSignal — TraCI Control Loop
================================
This is the heart of the simulation pipeline.

What this script does:
  1. Launches SUMO (GUI or headless) automatically
  2. Connects via TraCI every simulation step
  3. Reads full network state (queue, speed, waiting) for all 36 intersections
  4. Detects emergency vehicles by ID prefix
  5. Runs Dijkstra to compute EV corridor and preempts signals
  6. Logs all state data to CSV for BiLSTM training later
  7. Prints a live dashboard to the console
  8. Saves a summary plot at the end

How to run:
  # GUI mode (watch the simulation):
  python scripts/traciloop.py --gui

  # Headless mode (fast, for training):
  python scripts/traciloop.py

  # Inject an incident at a specific time:
  python scripts/traciloop.py --incident --incident-time 600 --incident-lane e_2_2_2_3_0

Requirements:
  pip install traci numpy pandas matplotlib networkx
  SUMO must be installed and on your PATH (sumo / sumo-gui commands work)
"""

