class IncidentInjector:
    """
    Injects simulated traffic incidents by stopping vehicles on a target lane.
    Used to generate labelled training data for the BiLSTM incident detector.

    An incident is represented as:
      - A target lane where vehicles are stopped (speed = 0)
      - A start time and (optional) end time
    """
    def __init__(self, target_lane, start_time, end_time=None, label="blockage"):
        self.target_lane = target_lane
        self.start_time  = start_time
        self.end_time    = end_time if end_time else start_time + 300  # 5 min default
        self.label       = label
        self.active      = False
        self.affected_vehicles = set()

    def step(self, sim_time):
        """Called every simulation step. Returns True if incident is currently active."""
        if sim_time >= self.start_time and sim_time <= self.end_time:
            if not self.active:
                print(f"\n  💥 INCIDENT INJECTED at t={sim_time}s | Lane: {self.target_lane}")
                self.active = True

            # Stop all vehicles on the target lane
            try:
                veh_ids = traci.lane.getLastStepVehicleIDs(self.target_lane)
                for vid in veh_ids:
                    if vid not in self.affected_vehicles:
                        try:
                            traci.vehicle.setSpeed(vid, 0.0)
                            self.affected_vehicles.add(vid)
                        except traci.exceptions.TraCIException:
                            pass
            except traci.exceptions.TraCIException:
                pass

            return True

        elif self.active and sim_time > self.end_time:
            # Release stopped vehicles
            print(f"\n  ✓ Incident cleared at t={sim_time}s")
            for vid in self.affected_vehicles:
                try:
                    traci.vehicle.setSpeed(vid, -1)  # -1 = restore normal speed
                except Exception:
                    pass
            self.affected_vehicles.clear()
            self.active = False

        return False


